/*使用mul函数的简单示例，输出为multiplication of 5 and 10 is 50*/
#include<stdio.h>
int mul(int a, int b);
int main()														/*说明这是主函数*/
{
	int a, b, c;
	a = 5;
	b = 10;
	c = mul(a, b);												/*c为mul(a,b)的值*/
	printf("multiplication of %d and %d is %d", a, b, c);		/*说明输出的是a,b,c三个数的值*/
	return 0;
}
int mul(int x, int y)											/*说明这是mul函数*/
{
	int p;
	p = x * y;
	return (p);

}